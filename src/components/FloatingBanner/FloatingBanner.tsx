type ModalType = 'consult' | 'estimate' | null

export default function FloatingBanner() {
  const [modal, setModal] = useState<ModalType>(null)
  const pathname = usePathname()
  const isAllowedPage = pathname === '/' || pathname.startsWith('/program')
  const showMobileBar = isAllowedPage

  if (!isAllowedPage) return null

  return (
    <>
      <div className={`${styles.banner} ${showMobileBar ? styles.banner_mobile_on : ''}`}>
        <button className={styles.item} onClick={() => setModal('consult')}>
          <div className={styles.icon}><ChatIcon /></div>
          <span className={styles.label}>상담예약</span>
        </button>
        <div className={styles.divider} />
        <button className={styles.item} onClick={() => setModal('estimate')}>
          <div className={styles.icon}><DocIcon /></div>
          <span className={styles.label}>견적요청</span>
        </button>
        <div className={styles.divider} />
        <button
          className={styles.item}
          onClick={() => {
            const el = document.getElementById('review')
            if (el) {
              el.scrollIntoView({ behavior: 'smooth' })
            } else {
              window.location.href = '/#review'
            }
          }}
        >
          <div className={styles.icon}><StarIcon /></div>
          <span className={styles.label}>유학후기</span>
        </button>
      </div>
      {modal === 'consult' && (
        <ConsultModal title="간편상담신청" type="consult" onClose={() => setModal(null)} />
      )}
      {modal === 'estimate' && (
        <ConsultModal title="견적요청신청" type="estimate" onClose={() => setModal(null)} />
      )}
    </>
  )
}
